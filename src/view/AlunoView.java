    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        var areaAt = filtroAreaAt.getText();
        var nomeVaga = filtroNome.getText();
        var modalidade = filtroModalidade.getText();
        var nomeEmp = filtroNomeEmp.getText();
        var cidade = filtroCidade.getText();
        var estado = filtroEstado.getText();
        
        var vagasFiltradas = system.getVagasFiltradas(currentAluno);
        
        if(!areaAt.isBlank())
            vagasFiltradas = system.filtraVagasPorArea(vagasFiltradas, areaAt);

         if(!nomeVaga.isBlank())
            vagasFiltradas = system.filtraVagasPorNome(vagasFiltradas, nomeVaga);

         if(!modalidade.isBlank())
            vagasFiltradas = system.filtraVagasPorModalidade(vagasFiltradas, modalidade);

         if(!nomeEmp.isBlank())
            vagasFiltradas = system.filtraVagasPorNomeEmpresa(vagasFiltradas, nomeEmp);

         if(!cidade.isBlank())
            vagasFiltradas = system.filtraVagasPorCidade(vagasFiltradas, cidade);

         if(!estado.isBlank())
            vagasFiltradas = system.filtraVagasPorEstado(vagasFiltradas, estado);

         this.vagas = vagasFiltradas;
         update();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnFiltrarActionPerformed

