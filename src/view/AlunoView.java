    private void miCandVagaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miCandVagaActionPerformed
        vagasComboModel.clear();
        var vagasDiponiveis = system.getVagasFiltradas(currentAluno);
        if(vagasDiponiveis.isEmpty()){
            JOptionPane.showMessageDialog(this, "Não existem vagas disponíveis!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        vagasComboModel.addListVaga(vagasDiponiveis);
        tareaVagas.setText("");
        comboVaga.setVisible(true);
        labelVaga.setVisible(true);
        btnCandidatarAluno.setVisible(true);
        update();
    }//GEN-LAST:event_miCandVagaActionPerformed

